    <h1>Privacy Policy for Mindway EAP</h1>
    <p><strong>Last updated:</strong> 13 April 2025</p>
    <p>Mindway Group PTY LTD, trading as Mindway EAP (ABN: 29682230075) ("Mindway EAP," "we," "us," or "our"), is committed to protecting your privacy. This Privacy Policy outlines how we collect, use, store, and safeguard your personal information when you use the Mindway EAP services (the "App").</p>

    <h2>1. Key Definitions</h2>
    <ul>
        <li><strong>Mobile Application:</strong> The Mindway EAP App designed for mobile devices (iOS, Android, etc.).</li>
        <li><strong>Personal Data:</strong> Any information about you that identifies you, including data you provide or data collected automatically.</li>
        <li><strong>User:</strong> Any person who downloads, activates, or uses the App and/or utilises counselling services.</li>
        <li><strong>Cookies:</strong> Small files used to enhance functionality and performance.</li>
        <li><strong>We/Us/Our:</strong> Mindway Group PTY LTD, trading as Mindway EAP, located in Victoria, Australia.</li>
    </ul>

    <h2>2. Applicability</h2>
    <p>This Privacy Policy applies to all personal data collected through the App and/or counselling services. By downloading, installing, or using the App, you acknowledge and agree to the terms outlined herein. If you do not agree with this policy, you must immediately cease using the App and its services.</p>

    <h2>3. Data We Collect</h2>
    <h3>3.1 Device Data</h3>
    <p>Device ID, IP address, operating system, and network details. This helps us optimise the App’s performance across various devices and platforms.</p>
    <h3>3.2 Voluntary Data</h3>
    <p>Information provided during signup or usage, such as your name, email address, phone number, notes, journal entries, and any other data you choose to submit through the App.</p>
    <h3>3.3 Employer Data Sharing on Signup</h3>
    <p>Your name and email address are shared with your employer during the signup process to manage access to Mindway EAP services. This occurs regardless of whether you subsequently use counselling services. Some employers may also provide departmental breakdowns to enhance the allocation of resources and services.</p>
    <h3>3.4 First-Time Client Form</h3>
    <p>Information submitted via the First-Time Client Form is shared with your assigned counsellor. This data remains strictly confidential and is used solely for providing tailored counselling services.</p>
    <h3>3.5 Cookies</h3>
    <p>Cookies are utilised to enhance the user experience, track app performance, and improve functionality. These may include both session cookies (temporary) and persistent cookies (stored for future visits).</p>

    <h2>4. Purpose of Collecting Data</h2>
    <p>We process your data for the following purposes:</p>
    <ul>
        <li>To enhance your user experience and analyse app usage patterns.</li>
        <li>To provide technical and customer support, ensuring seamless operation of the App.</li>
        <li>To communicate updates or changes related to your account or the App’s features.</li>
        <li>To securely store notes, journal entries, and other personal data submitted through the App.</li>
        <li>To validate and log counselling sessions for accurate record-keeping and compliance.</li>
        <li>To monitor app performance and usage trends using Google Analytics, helping us continuously improve service quality and user experience.</li>
    </ul>
    <h3>4.1 Confidentiality Exception</h3>
    <p>Aggregated and anonymised data, such as usage trends or general statistics (e.g., "percentage of employees citing 'work stress'"), may be shared with employers to provide insights into workforce wellbeing. Such data will never include identifiable personal information.</p>

    <h2>5. Data Security</h2>
    <p>We implement industry-leading measures to safeguard your data, including:</p>
    <ul>
        <li>Encryption of sensitive information during data transmission and storage.</li>
        <li>Regular updates and security patches to minimise vulnerabilities.</li>
        <li>Stringent access controls to ensure data is accessed only by authorised personnel.</li>
    </ul>
    <p>While we strive for robust security, no system is entirely infallible. By using the App, you acknowledge the inherent risks associated with data transmission over the internet.</p>

    <h2>6. Data Retention</h2>
    <p>We retain data only as long as necessary or as required by law. Specific retention periods include:</p>
    <ul>
        <li><strong>Counselling Session Data:</strong> Retained for seven (7) years from the last interaction to comply with legal and professional standards.</li>
        <li><strong>User Account Data:</strong> Retained while the account remains active and deleted within thirty (30) days after account closure, unless retention is required by law.</li>
    </ul>
    <p>To request data deletion, please contact <a href="mailto:support@mindwayeap.com.au">support@mindwayeap.com.au</a>.</p>

    <h2>7. Confidentiality of EAP Services</h2>
    <p>We prioritise the confidentiality of our Employee Assistance Program services. Identifiable information from counselling sessions is not shared with employers under any circumstances, unless:</p>
    <ul>
        <li>You provide written consent for disclosure.</li>
        <li>Disclosure is legally required (e.g., court orders).</li>
        <li>Disclosure is necessary to prevent serious harm to yourself or others.</li>
        <li>There is evidence or suspicion of child or elder abuse.</li>
    </ul>
    <h3>7.1 Data Not Shared with Employers</h3>
    <ul>
        <li>Counselling session details, including notes taken.</li>
        <li>Individual app usage data or counselling usage.</li>
        <li>Specific personal information shared during counselling sessions.</li>
    </ul>
    <h3>7.2 Limited Exceptions</h3>
    <ul>
        <li>Aggregated or anonymized data trends, such as session reasons may be shared with employers without revealing personal information.</li>
        <li>Billing-related information, such as the date of a counseling session, may be disclosed in compliance with legal or contractual obligations.</li>
        <li>Your name and email are shared with your employer during the signup process, but this is not linked to counselling usage.</li>
        <li>We may use this aggregated and anonymised data to give further breakdowns of company departments (if collected and applicable), which provides the employer with insights into specific areas of the business in relation to employee wellbeing.</li>
    </ul>

    <h2>8. User Rights</h2>
    <ul>
        <li><strong>Access:</strong> Request details on how we process your data and receive a copy of the data we hold about you.</li>
        <li><strong>Correction:</strong> Request updates or corrections to inaccurate or incomplete data.</li>
        <li><strong>Deletion:</strong> Delete your account directly via the App or by contacting us at <a href="mailto:support@mindwayeap.com.au">support@mindwayeap.com.au</a>.</li>
        <li><strong>Objection:</strong> Object to the processing of your data under specific circumstances, as outlined in applicable privacy laws.</li>
        <li><strong>Portability:</strong> Request a copy of your personal data in a structured, commonly used, and machine-readable format.</li>
    </ul>

    <h2>9. Data Storage</h2>
    <p>Your data is securely stored on industry-leading platforms to ensure its safety and integrity. Specifically:</p>
    <ul>
        <li><strong>AWS (Amazon Web Services):</strong> A globally trusted hosting provider offering advanced security measures, including encryption and access control.</li>
        <li><strong>Firebase (Google):</strong> A secure and reliable platform for data management, known for its robust privacy and security features.</li>
        <li><strong>Google Analytics:</strong> Used for analysing app usage, performance, and user behaviour in an aggregated, non-identifiable form. Data collected through Google Analytics is subject to strict access and retention controls.</li>
    </ul>
    <p><strong>Third-Party Services:</strong></p>
    <ul>
        <li><strong>Brevo:</strong> Used to facilitate email delivery and communication messaging. Brevo adheres to stringent privacy and security standards to protect your information.</li>
        <li><strong>CognitoForms:</strong> Utilised for consent and intake forms, ensuring secure handling of sensitive data in compliance with privacy laws and regulations.</li>
    </ul>

    <h2>10. Minors</h2>
    <p>The App and its services are intended for users aged 18 years and over. Users under the age of 18 must discontinue use immediately. We do not knowingly collect or store personal data from minors.</p>

    <h2>11. Data Breaches</h2>
    <p>In the event of a data breach:</p>
    <ul>
        <li>We will promptly assess the impact and notify affected individuals if there is a risk of serious harm within 72 hours.</li>
        <li>We will report the breach to the relevant data protection authority or government body based on the jurisdiction of the affected individuals, including but not limited to the OAIC (Australia) or ICO (UK).</li>
        <li>We will provide updates as necessary and take steps to mitigate the impact of the breach.</li>
    </ul>

